#!/usr/bin/env python3
"""
Module to calculate the floor of a float number.
"""
import math

def floor(n: float) -> int:
    """
    Function to calculate the floor of a float number.

    Args:
        n (float): The float number.

    Returns:
        int: The floor value of the float number (rounded down to the nearest integer).
    """
    return math.floor(n)

