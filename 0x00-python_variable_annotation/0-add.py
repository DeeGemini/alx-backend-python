#!/usr/bin/env python3
"""
Module to perform addition operation.
"""

def add(a: float, b: float) -> float:
    """
    Function to add two float numbers and return their sum.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of a and b.
    """
    return a + b

